                     <h2 class="panel-title">Branson on Strive</h2>
                  </div>
                  <div class="panel-body">
                     <div style="width:100%; margin-bottom:20px;"><img src="images/SirRichard.jpg" /></div>
                     <p style="text-align:justify"><strong>&ldquo;As I&rsquo;m writing about these businesses, ......I&rsquo;m reminded of a conversation I had with Strive Masiyiwa, chairman and founder of Econet, one of the largest telecommunication companies in the world. ...... Strive is passionate about Africa gaining full economic freedom. ....he wanted to make an even bigger difference.&rdquo;</strong></p>
                     <p style="text-align:justify"><strong>&ldquo;one of the people responsible for this communications revolution is one of my co-founders in the carbon war room, Strive Masiyiwa.&rdquo;</strong></p>
                     <p style="text-align:justify"><strong>&ldquo;</strong>In 1965, when Strive was seven years old, Rhodesia&rsquo;s leader Ian Smith authored a unilateral declaration of independence from Britain. Strive&rsquo;s parents left the country, sending their son to a boarding school in Edinburgh. Strive longed to return home and he planned eventually to go to Zambia and join the guerrillas, just across the border, to fight in the armed struggle against Smith&rsquo;s ruling white minority. But then he talked to his cousin- who ran a training camp for the guerrilla army that would one day become Robert Mugabe&rsquo;s&nbsp; Zanu &ndash;PF party- who told him he was too late: the war was all but won. <strong>What the new country needed now, more than soldiers, was educated people. </strong>Strive listened to his cousin&#39;s wise words and stayed in Britain to study, earned his degree, and, returning to what was now Zimbabwe, he went to work for the national telephone company. Growing emerging markets and mobile phone networks have become a marriage made in heaven. Mobile networks are quicker and cheaper to buy than landlines. In 1993, Strive had no trouble acquiring the finance needed to launch his first mobile phone company. The trouble was that Mugabe did not want to grant any licences for new mobile phone companies. Strives legal battles with Mugabe came to a head in 1996 when the presidential decree banned private cell phone operations. But Strive did not give up and decided to take the government to court. In December 1997 he won his case and proceeded to set up Econet as a successful mobile phone company in Zimbabwe.</p>
                     <p style="text-align:justify"><strong>By then, however, Strives vision had grown. His ambitions had extended beyond his troubled country&#39;s frontiers, and from his headquarters in Johannesburg he decided to create a truly multinational African Business, one that today spans seven countries,</strong> <strong><strong>boasts more than twenty-five million customers and generates an estimated $3 Billion (&pound;1.8 Billion) in annual revenues. Econet is a celebrated example of African entrepreneurship and is changing people&rsquo;s lives for the better every day.</strong></strong></p>
                     &nbsp;
                     <div style="width:73%; float:left; text-align:justify; margin-top:15px;">
                        <p><strong>Strive also has a deep sense of responsibility about giving something back to the community.</strong> Through the Capernaum Trust, which he set up with his wife, profits have so far provided education for 26,000 orphans. Strive sits on the board of Trusties of the Rockefeller Foundation and works with Kofi Annan&rsquo;s Alliance for a Green Revolution in Africa &ndash; as well as being the co founder with me and seven other entrepreneurs in the Carbon War room.</p>
                        <p><strong>Just as important as what he is doing individually are the values Strive has instilled in his company.</strong>The firm provides counselling and antiretroviral drugs for its employees and their families who are living with HIV/AIDS. It spreads entrepreneurial opportunity, investing in heavily in community phone shops and payphones throughout its local markets to bring connectivity to people who would normally not be able to afford it. <strong>In brining telecommunications to those communities, Econet is empowering local populations, giving them the chance to be entrepreneurs by running these businesses</strong>. Econet&rsquo;s phones, like millions of others around the world, are becoming doctors, insurance companies, banks and so much more.</p>
                        <p>The opportunity to use the mobile phone to drive change in the world is enormous, with over 5.3 Billion mobile phone users in the world and 90% of the world&rsquo;s population able to get access to a mobile service. Take health: by 2015, five hundred million people will be using mobile health applications around the world. This simple device is transforming our response to health issues, from prevention of diseases such as HIV with education through mobile games, to diagnostic and treatment support, to outbreak tracking, the list goes on...&rdquo;</p>
