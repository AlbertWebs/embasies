         <p><strong>ECONET IS A 30&nbsp;YEAR-OLD ENTREPRENEURIAL TECHNOLOGY GROUP</strong></p>
         <p>Econet is a technology focused Group with an entrepreneurial spirit. Our purpose is to empower Africans with technology for economic prosperity. Led by our Founder & Executive Chairman, Strive Masiyiwa and an experienced management teams we build and operate businesses that use technology to address everyday consumer and business needs in Africa.</p>
         <p>As an active owner and operator, Econet believes in delivering both shareholder and social value by building long-term sustainable businesses that contribute positively to society. We invest in Africa, with a focus on Sub Saharan Africa.</p>
         <p>Econet was founded by Strive Masiyiwa and started operations in 1998 with mobile phone companies in Botswana and Zimbabwe. Today we operate in 23 African markets through two strategic business units&nbsp; which cover mobile telephony (<a href="https://www.econetwireless.com/">Econet Wireless</a>), digital technology and platforms (<a href="https://cassavatechnologies.com/">Cassava Technologies</a>) and our corporate social enterprises (<a href="https://deltaphilanthropies.org/">Delta Philanthropies</a> and <a href="https://www.higherlifefoundation.com/">Higherlife Foundation</a>).</p>
