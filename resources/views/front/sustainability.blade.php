<div class="lay-width innercontent"><p><em><strong>Creating value for sustainable societies and endless possibilities</strong></em></p>

<p>At Econet we truly believe that our corporate duty extends beyond corporate profits and that our success cannot be measured by financial performance alone. For that reason, we see our broader purpose as a positive driving force for social, environmental as well as economic benefit for our stakeholders worldwide. To help build a sustainable future, we will continue to deliver products and services that create value and contribute significantly to improving the quality of life and sustainable development of the people in the communities where we work.</p>

<p>We have always been aware of our business&rsquo; duty to contribute to improved livelihoods of every person we reach and to preserve the environment.&nbsp; Since the adoption of the United Nations Sustainable Development Goals (SDGs) in September 2015, as a call to action to work together to end poverty, protect the planet and ensure prosperity for all, we have begun to intentionally integrate sustainability into all our business strategies.&nbsp;</p>

<p>As a pioneering company, not only will we drive sustainability related initiatives to transform our people, but we will continue to identify needs and problems in our communities through stakeholder engagement and use latest digital innovation technologies to create commercially viable and relevant societal solutions. This will empower our people and improve their livelihoods for a better tomorrow.</p>

<p>In a bid to strengthen our sustainability drive, we are moving beyond corporate social responsibility to corporate social innovation and investment which is more sustainable. Our social role is therefore constantly being reviewed in order to remain relevant to our societies by integrating economic, environmental and social considerations in all decisions affecting our operations to ensure efficient resource utilization for the benefit of current and future generations.</p>

<p>Visit <a href="https://deltaphilanthropies.org/">Delta Philanthropies</a>&nbsp;and&nbsp;<a href="https://www.higherlifefoundation.com/">Higherlife Foundation</a>&nbsp;to learn more about our corporate social enterprises.</p>
