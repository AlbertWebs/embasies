                  <p>Strive Masiyiwa is a telecommunications and technology entrepreneur, investor, and philanthropist. In his nearly four decades as an entrepreneur, he has started and invested in businesses throughout the world including Africa, the European Union, India, Latin America, the Middle East, New Zealand, the UK, and the USA.</p>
                  <p style="font-size:20px; line-height:25px;"><img src="https://www.econetafrica.com/images/abt_img.jpg" style="display:inline-block; margin-bottom:9px;" /><br />
                     <strong>Strive Masiyiwa<br />
                     Chairman and Founder, Econet&nbsp;</strong>
                  </p>
                  <p>Some of his most prominent start-up companies and investments include: Mascom Wireless Botswana, Econet Wireless Zimbabwe, Econet Wireless Nigeria (now Airtel Nigeria), Liquid Intelligent Technologies Group, Econet Wireless New Zealand (now 3 Degrees Mobile), Trilogy Capital Partners Canada (early investor), Cassava Technologies, Africa Data Centres, Sasai Fintech, and Telrad Group Israel. He continues to invest privately through his investment companies, and family office.</p>
                  <p>Born in Zimbabwe in 1961, he left the country as a child and returned after Independence in 1984, working briefly as a telecoms engineer before setting up his first business in 1986. He emigrated from Zimbabwe in 2000, living with his family in South Africa until 2010. They then moved to the UK in 2010 from where he runs a global portfolio in more than 40 countries on four continents.<br />
                     &nbsp;<br />
                     His family foundations, Higherlife Foundation and Delta Philanthropies, have supported over 300,000 scholarships to African children, public health, girls empowerment, youth mentorship, rural transformation, and other critical social impact initiatives in Africa. The foundations are managed by his co-founder wife, Tsitsi, and their oldest daughter, Elizabeth.<br />
                     &nbsp;<br />
                     Strive graduated with a Bachelor&rsquo;s degree in Electrical and Electronic Engineering from the University of Cardiff and has received several honourary doctorates. Committed Christians, Strive and Tsitsi have six adult children. They are signatories of the Giving Pledge.
                  </p>
